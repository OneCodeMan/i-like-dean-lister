import Foundation
import UIKit

public extension UIFont {
    
    static var helveticaNormal: UIFont {
        return UIFont(name: "Helvetica", size: 20.0)!
    }
    
    static var helveticaBold: UIFont {
        return UIFont(name: "Helvetica-Bold", size: 24.0)!
    }
    
    static var helveticaItalic: UIFont {
        return UIFont(name: "Helvetica-Oblique", size: 18.0)!
    }
    
    static var helveticaLight: UIFont {
        return UIFont(name: "Helvetica-Light", size: 18.0)!
    }
    
}
